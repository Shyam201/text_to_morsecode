def text_to_morse(text):
    morse_dict = {'a': '.-', 'b': '-...', 'c': '-.-.', 'd': '--.',
                  'e': '.', 'f': '..-.', 'g': '--.', 'h': '....',
                  'i': '..', 'j': '.---', 'k': '-.-', 'l': '.-..',
                  'm': '--', 'n': '-.', 'o': '---', 'p': '.--.',
                  'q': '--.-', 'r': '.-.', 's': '...', 't': '-',
                  'u': '..-', 'v': '...-', 'w': '.--', 'x': '-..-',
                  'y': '-.--', 'z': '--..', '1': '.----', '2': '..---',
                  '3': '...--', '4': '....-', '5': '.....', '6': '-....',
                  '7': '--...', '8': '---..', '9': '----.', '0': '-----'}

    morse = ''
    for char in text:
        if char != " ":
            morse += morse_dict[char] + ' '
        else:
            morse += "/ "
    return morse


text = input("Enter text: ").lower()

print(text_to_morse(text))

